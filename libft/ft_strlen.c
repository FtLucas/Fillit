#include "libft.h"

size_t		ft_strlen(const char *str)
{
	size_t i;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i + 1] == '\0')
			i++;
		else if (str[i + 2] == '\0')
			i += 2;
		else if (str[i + 3] == '\0')
			i += 3;
		i += 4;
	}
	return (i);
}
