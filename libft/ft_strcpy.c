#include "libft.h"

char	*ft_strcpy(char *dst, const char *src)
{
	size_t i;

	i = 0;
	while (*src != '\0')
	{
		*dst = *src;
		dst++;
		src++;
		i++;
	}
	if (*src == '\0')
		*dst = '\0';
	return (dst - i);
}
